@Configuration
@EnableSwagger2
public class Swagger2 {

	@Bean
	public Docket createRestApi() {
		return new Docket(DocumentationType.SWAGGER_2).apiInfo(apiInfo()).select()
				.apis(RequestHandlerSelectors.basePackage("com.ddmc.controller")).paths(PathSelectors.any()).build();
	}

	private ApiInfo apiInfo() {
		return new ApiInfoBuilder().title("kafka Api").description("叮咚用户行为抓取接口文档</br>http://www.ddmc.com/*")
				.termsOfServiceUrl("http://www.ddmc.com/*").contact("com.ddmc").version("1.0").build();
	}

}
